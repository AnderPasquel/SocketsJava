    public void bilgi() throws IOException {
        InetAddress address;
        try {
            address = InetAddress.getLocalHost();
            jLabel4.setText(System.getProperty("os.name"));
            jLabel8.setText(System.getProperty("user.name"));
            jLabel9.setText(sistemTarihiniGetir("yyyy/MM/dd H:mm:ss"));
            jLabel2.setText(address.getHostAddress().toString());

            if (Dosya.exists() && Dosya.isFile()) {
                JOptionPane.showMessageDialog(null, "El archivo ya existe");
            } else {
                JOptionPane.showMessageDialog(null, "Ningún archivo nuevo creado");
                Dosya.createNewFile();

            }

        } catch (UnknownHostException ex) {
            Logger.getLogger(Server.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public String sistemTarihiniGetir(String tarihFormati) {
        Calendar takvim = Calendar.getInstance();
        SimpleDateFormat sdf = new SimpleDateFormat(tarihFormati);
        return sdf.format(takvim.getTime());
    }

