    protected static void tablovericek() throws IOException {

        freading = new FileReader("kayit.server");
        Buffread = new BufferedReader(freading);
        String line = null;
        String temp[] = new String[10];
        String sutunbaslik[] = {"İp Address", "Nombre del sistema", "Sistema operativo", "Hora del sistema", "Memoria libre (MB)", "CPU (%)", "Disco libre (MB)"};
        Object Veri[][] = {};
        dtm = new DefaultTableModel(Veri, sutunbaslik);

        while ((line = Buffread.readLine()) != null) {
            String[] str2 = line.split(";");
            for (int i = 0; i < 7; i++) {
                temp[i] = str2[i];

            }
            Object veri2[] = {temp[0], temp[1], temp[2], temp[3], temp[4], temp[5], temp[6]};

            dtm.insertRow(0, veri2);

        }
        jTable1.setModel(dtm);
        freading.close();
    }
